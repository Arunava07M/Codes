#include<iostream>
using namespace std;

int main(){
    //Decrement unary Operator
    
    //a--
    int a = 10;

    int b = a--;

    cout << "b = " << b << endl;
    cout << "a = " << a << endl;
    
    //--c
    int c = 10;

    int d = --c;

    cout << "d = " << d << endl;
    cout << "c = " << c << endl;
    return 0;
}