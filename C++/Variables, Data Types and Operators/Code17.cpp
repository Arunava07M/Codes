#include<iostream>
using namespace std;

int main(){
    int a = 10;
    a += 5; // a = a + 5
    cout << "a = " << a << endl;

    int b = 10;
    b -= 5; // b = b - 5
    cout << "b = " << b << endl;

    int c = 10;
    c *= 5; // c = c * 5
    cout << "c = " << c << endl;

    int d = 10;
    d /= 5; // d = d / 5
    cout << "d = " << d << endl;

    return 0;

}