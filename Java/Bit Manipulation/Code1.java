public class Code1{
    public static void main(String[] args) {
        System.out.println("Binary AND(&) = " + (5 & 6));
        System.out.println("Binary OR(|) = " + (5 | 6));
        System.out.println("Binary XOR(^) = " + (5 ^ 6));
        System.out.println("Binary One 's Complement(~) of 5 = " + (~5));
        System.out.println("Binary Left Shift of 5(<<) = " + (5 << 2));
        System.out.println("Binary Right Shift of 6(>>) = " + (6 >> 1));
        
    }
}