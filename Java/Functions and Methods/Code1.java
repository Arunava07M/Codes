public class Code1{
    public static void Hello(){
        System.out.println("Hello World");
        return;
        // this is a function of return type void 
        // writing return was not required
        // because in void return type, return is not required
        // but it is your choice whether you want to write it or not
    }
    public static void main(String[] args) {
       Hello(); // I have called the function
    }
}