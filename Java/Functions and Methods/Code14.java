public class Code14 {

    public static int sum(int a, int b){
        return a + b;
    }

    public static float sum(float a, float b){
        return a + b;
    }
    public static void main(String[] args) {
        System.out.println("Integer Sum = " + sum(5, 6));
        System.out.println("Float Sum = " + sum(4.6f,5.3f));
    }
}
